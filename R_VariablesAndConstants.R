#Boolean Variables
a=TRUE
print(a)
print(class(a))

#Integer Variables
A = 14L
print(A)
print(class(A))

#Floating Point Variables
x=13.4
print(x)
print(class(x))

#Character Variables
alphabet = "a"
print(alphabet)
print(class(alphabet))

#String Variables
msg = "Welcome to R"
print(msg)
print(class(msg))